class ReviewsController < ApplicationController
  def index
    @burger = Burger.find(params[:burger_id])
    @reviews = @burger.reviews
  end

  def new
    if current_user.present?
      @burger = Burger.find(params[:burger_id])
      @review = Review.new
    else
      flash[:error] = "Please log in before trying to submit a review!"
      redirect_to new_user_session_path
    end
  end

  def create
    @burger = Burger.find(params[:burger_id])
    @review = Review.new(review_params)
    @review.burger = @burger
    @review.user = current_user
    burger_owner = @burger.user
    if @review.save
      UserMailer.review_added(burger_owner, @burger, @review).deliver
      flash[:notice] = "Review submitted successfully!"
      redirect_to burger_reviews_path(@burger)
    else
      @errors = @review.errors.full_messages
      render :new
    end
  end

  private

  def review_params
    params.require(:review).permit(
      :burger_rating,
      :body
    )
  end
end
